public class SwitchFun {
    public static void main(String[] args)
    {
        int ch;
        ch = 0;

        switch(ch)
        {
            case 1 : System.out.println("HELLO WORLD "+ch);
            break;
            case 2 : System.out.println("HELLO WORLD "+ch);
            break;
            default : System.out.println("HELLO WORLD "+"Default");
        }
    }
}
